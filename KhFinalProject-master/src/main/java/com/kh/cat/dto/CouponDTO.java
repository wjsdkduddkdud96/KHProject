package com.kh.cat.dto;

public class CouponDTO {
	private int coupon_code;
	private long coupon_price;
	private String coupon_name;
	
	public int getCoupon_code() {
		return coupon_code;
	}
	public void setCoupon_code(int coupon_code) {
		this.coupon_code = coupon_code;
	}
	public long getCoupon_price() {
		return coupon_price;
	}
	public void setCoupon_price(long coupon_price) {
		this.coupon_price = coupon_price;
	}
	public String getCoupon_name() {
		return coupon_name;
	}
	public void setCoupon_name(String coupon_name) {
		this.coupon_name = coupon_name;
	}
	
	
}
